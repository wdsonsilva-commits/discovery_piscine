#!/usr/bin/env python3

# Define a função "upcase_it", que recebe um parâmetro chamado "texto"
def upcase_it(texto):
    # return devolve o valor para quem chamou a função,
    # em vez de o mostrar diretamente (isso é diferente de print!)
    return texto.upper()


# Chama a função com "hello" e mostra o valor que ela devolveu
print(upcase_it("hello"))

